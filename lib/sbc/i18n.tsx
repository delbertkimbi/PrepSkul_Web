type SbcLanguageContextValue = {
  locale: SbcLocale
  setLocale: (locale: SbcLocale) => void
  t: (text: string) => string
}

const SbcLanguageContext = createContext<SbcLanguageContextValue | null>(null)

export function SbcLanguageProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<SbcLocale>("en")

  useEffect(() => {
    const saved = window.localStorage.getItem("sbc-locale")
    if (saved === "en" || saved === "fr") setLocaleState(saved)
  }, [])

  useEffect(() => {
    document.documentElement.lang = locale
  }, [locale])

  const setLocale = useCallback((next: SbcLocale) => {
    setLocaleState(next)
    window.localStorage.setItem("sbc-locale", next)
    document.documentElement.lang = next
  }, [])

  const t = useCallback((text: string) => locale === "fr" ? fr[text] ?? text : text, [locale])
  const value = useMemo(() => ({ locale, setLocale, t }), [locale, setLocale, t])

  return <SbcLanguageContext.Provider value={value}>{children}</SbcLanguageContext.Provider>
}

export function useSbcLanguage() {
  const value = useContext(SbcLanguageContext)
  if (!value) throw new Error("useSbcLanguage must be used inside SbcLanguageProvider")
  return value
}
